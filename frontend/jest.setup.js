// Mock crypto for security modules
Object.defineProperty(global, 'crypto', {
  value: {
    getRandomValues: (arr) => {
      for (let i = 0; i < arr.length; i++) {
        arr[i] = Math.floor(Math.random() * 256)
      }
      return arr
    },
    randomBytes: (length) => {
      const arr = new Uint8Array(length)
      return global.crypto.getRandomValues(arr)
    }
  }
})

